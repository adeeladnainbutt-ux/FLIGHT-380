// Get airline logo URL with fallback
export const getAirlineLogo = (airlineCode) => {
  if (!airlineCode) return null;
  const code = airlineCode.toUpperCase().trim();
  return airlineLogos[code] || `https://images.kiwi.com/airlines/64/${code}.png`;
};

// Airline Logo Component
export const AirlineLogo = ({ code, className = 'w-10 h-10' }) => {
  const logoUrl = getAirlineLogo(code);
  
  return (
    <div className={`${className} flex items-center justify-center bg-white rounded-lg border border-slate-200 p-1 overflow-hidden`}>
      <img 
        src={logoUrl} 
        alt={code}
        className="w-full h-full object-contain"
        onError={(e) => {
          e.target.style.display = 'none';
          e.target.parentElement.innerHTML = `<span class="text-xs font-bold text-slate-600">${code}</span>`;
        }}
      />
    </div>
  );
};
